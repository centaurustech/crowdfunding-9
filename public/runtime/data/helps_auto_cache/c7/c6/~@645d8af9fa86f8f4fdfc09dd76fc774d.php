<span style="color:#B22222;"><span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;"><strong>6、“目标用户或客户群体目前困扰或需求定位”</strong></span><span style="color:#404040;line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">中，说明清楚市场需求具体是什么，其市场容量及前景如何，目前已有的供需关系如何，存在哪些不足等。建议可补充政府对该行业的相关支持政策；</span></span><span></span>
<div>
	<span style="line-height:1.5;color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>7、“满足目标用户或客户需求的产品或服务模式说明”</strong></span><span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">中，说明你的产品和服务具体怎么满足上文描述的需求，改进了哪些不足。建议介绍该行业所在的产业链条上的分工情况，以及你处于该链条上的哪个环节；</span>
</div>
<span style="color:#B22222;"><span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;"><strong>8、“项目赢利模式说明”</strong></span><span style="color:#404040;line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">中，详细介绍你跟谁收费，收多少，怎么收，有什么前提条件？如果你的盈利模式已经在发生，需说明收入和现金流的大概情况。如果只是计划，尚未发生，说明你要产生收入需实现哪些前提条件？</span></span><span></span><span style="color:#B22222;"><span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;"><strong>9、“市场主要同行或竞争对手概述”</strong></span><span style="color:#404040;line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">中，列出你的直接竞争对手并做简述，并说明你与他们的差异化或你的竞争优势。务必在这里说明清楚，和他们相比，你的市场空间在哪里？你为什么认为你能吸引客户来用你的产品或服务，而不是去用他们的？</span></span><span></span><span style="color:#B22222;"><span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;"><strong>10、“项目主要核心竞争力说明”</strong></span><span style="color:#404040;line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">中， 需列出一二三，描述具体的优势，不要讲空话和大话。务必在这里说明或解释清楚，你这个行业的门槛不高，既然你能做，那其他人也能做，你怎么看待这个问题， 或者你准备如何应对？这将是所有投资人最关心的问题。此外，建议列举与你同处该细分行业内的同类公司（比如你的竞争对手）的成功融资案例，有利于增强投资 人信心；另外，建议创业者在此追加一句“各位投资人在报价前，可先单独与本人电话沟通协商，然后再到股权众筹上报价，谢谢！”；</span></span><span></span><span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>11、“创业团队”：</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（1）关于创始团队的股权结构：由于股权众筹目前主要服务于天使阶段的创业项目，所以一般要求创始人本人的占股不低于60%。即使是合伙创业，也需符合一方占60%以上，其余各方占剩余股份。如不符合该要求，请自行调整股权结构后再行申请。请注意，填写“所占股份”时，只需填写数字即可，不用填百分号，比如占股百分之百，只需填“100”，而不是“100%”。</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（2）表格中的“实际出资金额”，是指项目启动至今，各位股东实际投入的现金金额，而不是指注册资本，所以不计实物和知识产权等投入。</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（3）关于团队成员的介绍：需按照写简历的格式和详细程度来填写，需至少介绍团队成员的出生年月、性别、毕业院校、工作经历、工作成就、个人优势、过往的创业经历和心得、为什么会从事目前这个 行业的创业、目前各人的分工情况等。另外，请务必说明核心团队成员已合作多久，通过什么渠道认识的，目前正在或未来需要在哪些方面加强磨合，并举例说明？ 丰富创业团队的介绍，有利于加深投资人对你的了解，增强投资信心。注意出资金额的单位是万元；</span>
</div>
<span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>12、“历史执行情况”：</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（1）“阶段达成目标描述”中，应说明目前公司取得的成果或达到的目标，如产品研发到什么阶段、客户数达到多少、实现了什么突破等。建议说明目前有多少客户或用户，每月收入有多少，订单量有多少？目前每月开支金额多少，主要是什么支出等？</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（2）详细介绍“完成阶段目标的主要措施、方法、手段”。建议补充说明创业至今，团队已累计投入多少资金，目前有多少负债？</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（3）补充“阶段收入”和“阶段开支”的明细情况，如销售了多少产品，客户是谁？办公地每月租金多少，场地面积多大，付款方式怎样？目前有多少员工，各岗位的工资是多少，公司平均工资是多少？如果现在不发工资，应列上人员工资为0，并说明你预计何时开始发工资？</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（4）注意金额的单位是元；</span>
</div>
<span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>13、“未来计划”：</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（1）建议至少描述2个阶段，第一阶段建议从现在开始，到你产品的正式发布时间或开店营业时间；第二个阶段是从产品发布或开店营业开始，到公司实现盈亏平衡。建议预测时间在未来一年到一年半左右即可；</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（2）“阶段达成目标描述”中，需列出具体数据指标，比如粗略预计多长时间达到多少客户或用户，需多久或要什么条件才能达到盈亏平衡？如果无法预测，请说明理由。建议列出正在跟进、或计划跟进的大客户名单或订单。</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（3）详细介绍“完成阶段目标的主要措施、方法、手段”，不要讲空话大话。另外，建议说明你目前最需要的支持或资源是什么，方便有些投资人可以对接合作。</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（4）补充“阶段收入”和“阶段开支”的明细情况，这些预测数据需合理靠谱，股权众筹将重点审核该数据的可实现性。对于收入的预测不能过于乐观，需说明预测依据，并与同类公司的业务情况做对比，说明该预测数据的合理 性。开支明细也应详细描述，比如租房面积，每月租金，付款方式是怎样？还有人员工资，如果不发工资，你应列上人员工资为0，同时说明你预计企业发展到什么 阶段开始发工资。</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">（5）注意金额的单位是元；</span>
</div>
<span style="color:#B22222;"><span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;"><strong>14、“项目附件”</strong></span><span style="color:#404040;line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">中，需上传产品或服务的实景照，以及营业执照、组织机构代码证、税务登记证、创始人身份证的扫描件或照片，以及其它你认为有必要上传的材料；</span></span><span></span><span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>15、融资金额、出让股份和付款方式</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">仔细考虑您的融资金额和付款方式。股权众筹一般要求项目的融资金额在30-1000万之间（一般融资金额不超过团队未来一年到一年半左右的开支）。而出让股份需由投资人询价最终确定，询价范围限定在10%-30%之间。</span>
</div>
<span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>温馨提醒：</strong></span><span></span><span style="color:#B22222;">
<div>
	<strong><span style="font-family:'Microsoft YaHei';font-size:14px;">1、</span><strong><span style="font-family:'Microsoft YaHei';font-size:14px;">项目审核上线问题</span></strong></strong>
</div>
</span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">从收到您提交的项目，到回复审核意见，一般需要一周左右的时间。如果您的项目通过审核，我们会通知您签订居间服务协议，股权众筹在收到您签好的居间服务协议后，才会上线您的项目正式开始融资。在审核期间，您可以随时电话咨询审核进度。需要说明的是，并非所有的项目都可以通过审核上线融资，股权众筹有专门的项目评审团队，并设定了详细的项目评审标准，对于尚不成熟或亮点不足的项目，股权众筹有权拒绝申请；</span>
</div>
<span style="color:#B22222;">
<div>
	<strong><span style="font-family:'Microsoft YaHei';font-size:14px;">2、</span><strong><span style="font-family:'Microsoft YaHei';font-size:14px;">项目估值问题</span></strong></strong>
</div>
</span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">股权众筹对创业项目的估值不做评估，而是采取市场竞价机制，交由投资人评估报价。竞价方式为各投资人单独向创业者询价，以项目投后估值为竞价指标，对应的出让股份比例范围是10%-30%，创业者选择“拒绝或接受”，创业者也有权拒绝不询价的认投。创业者历次同意的最低估值（对应最高股权比例）为系统最终估值，项目未融资成功前投资人无法看到项目当前最新估值；</span>
</div>
<strong><span style="font-family:'Microsoft YaHei';font-size:14px;">3、成本费用问题</span></strong><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">创业者到股权众筹众筹融资只有一项成本，即项目融资成功后，股权众筹向创业者收取融资额度的5%作为服务费。如果项目没有融资成功，则不收取创业者任何费用，创业者仅付出了时间成本；</span>
</div>
<span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>4、融资时间问题</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">各个项目的众筹融资进度不一，根据我们过往的成功案例，有在一个礼拜内就融完的，也有在半年后才融完的。所以我们一般建议创业者至少做好等待四个月左右的准备；</span>
</div>
<span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>5、推进融资进度问题</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">股权众筹定位为独立第三方平台，不会主动向投资人推荐项目。创业者可以通过以下方式加快融资进度：</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">a、尽快确认领投人。一般领投人确认后，投资人会对项目更有信心，有利于加快融资进度。创业者获取领投人有两种途径：一是投资人认购项目后主动申请当领投 人；二是创业者主动邀请投资人担任领投人。股权众筹首页的“天使投资人”版块中有公布领投人名单，创业者可以主动联系和自荐项目；</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">b、实时更新“项目动态”。创业者可登录个人账户填写“项目动态”，“项目动态”的内容会实时展示在股权众筹首页。经常更新项目动态，有利于投资人了解项目的最新进展，并产生投资兴趣；</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">c、创业者个人积极推广。创业者可在自己的个人人脉圈中积极推广，例如微信朋友圈、个人微博、或者参加股权众筹组织的路演活动等等；</span>
</div>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">d、其它个性化方式。</span>
</div>
<span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>6、项目修改及撤销问题</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">项目提交审核通过后，就开始发布融资了，此后就不能再修改或是撤销项目，特别是融资金额、付款方式这些要点，所以请您务必事先考虑清楚自己的需求，切勿事后反悔，引起不必要的麻烦。如果项目发布四个月后无法完成融资，届时您可以选择撤销融资；</span>
</div>
<span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>7、投资期限及退出问题</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">由于投资人最后是以有限合伙企业的名义来入股创业者的公司，而根据相关规定，一般情况下有限合伙企业的寿命是5年，所以一般情况下创业者必须在5年内让投 资人退出。所以创业者在融资前，需预先考虑好投资人的退出方式。一般有这几种退出方式：上市、被并购、后续新投资人进入后转让老股东股份、回购等。</span>
</div>
<span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;"><strong>8、投资人关系管理问题</strong></span><span></span>
<div>
	<span style="line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">我们一般建议创业者在项目通过审核并发布后，尽快建立一个QQ群或微信群，并将该群号或二维码发布在“项目动态”中，方便投资人加入，提高沟通效率。</span>
</div>
