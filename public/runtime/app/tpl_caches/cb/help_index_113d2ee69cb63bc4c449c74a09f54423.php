	</div>
<strong><strong><span style="font-family:'Microsoft YaHei';font-size:14px;">2、项目名称简洁易懂，指明要点，比如“股权众筹网站（天使股权投资众筹平台）”；</span></strong></strong></span><strong><strong><span style="color:#B22222;font-family:'Microsoft YaHei';font-size:14px;">3、“企业项目简介”</span></strong><span style="font-weight:normal;line-height:1.5;font-family:'Microsoft YaHei';font-size:14px;">中，至少应包括以下内容：</span></strong>
	<div>
